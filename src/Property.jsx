class Property {
    Title;
    Province;
    City;
    Price;
    Rating;
    Img;
    constructor(title, province, city, price, rating, img) {
        this.Title = title;
        this.Province = province;
        this.City = city;
        this.Price = price;
        this.Rating = rating;
        this.Img = img;
    }
};

export default Property;